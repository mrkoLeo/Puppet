interface IResponse {
    /**
     * 是否成功
     */
    fun isSuccess(): Boolean

    /**
     * Code码
     */
    fun getCode(): Int

    /**
     * 信息
     */
    fun getMessage(): String
}

class BaseResponse<T> : IResponse {

    var mCode: Int = 0

    var mResult: T? = null

    var mMessage: String? = null

    companion object {

        const val CODE_SUCCESS = 0

        const val CODE_FAILED = -1

        fun <T> create(result: T, code: Int, message: String): BaseResponse<T> {
            val response = BaseResponse<T>()
            response.mCode = code
            response.mResult = result
            response.mMessage = message
            return response
        }
    }

    override fun isSuccess(): Boolean {
        return mCode == CODE_SUCCESS
    }

    override fun getCode(): Int {
        return mCode
    }

    override fun getMessage(): String {
        return mMessage ?: ""
    }
}