class RetrofitManager {
    val okHttpClient = OkHttpClient.Builder()
//        .addInterceptor(DuokeInterceptor())
//        .addInterceptor(InstabugOkhttpInterceptor())
        .retryOnConnectionFailure(true)
        .connectTimeout(60, TimeUnit.MILLISECONDS)
        .readTimeout(60, TimeUnit.MILLISECONDS)
        .writeTimeout(60, TimeUnit.MILLISECONDS)
        .build()

    var retrofit = Retrofit.Builder()
        .client(okHttpClient)
        .baseUrl("https://api.github.com/")
        .addConverterFactory(GsonConverterFactory.create())
        .addCallAdapterFactory(RxJava2CallAdapterFactory.create())
        .build()

    var service = retrofit.create(GitHubService::class.java)

}
