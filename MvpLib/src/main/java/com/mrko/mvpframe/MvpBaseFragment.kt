abstract class MvpBaseFragment<T : BasePresent> : BaseFragment() {
    protected lateinit var mPresenter: T

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        val t: Any? = getT(this, 0)
        if (t != null) {
            mPresenter = t as T
            mPresenter.attach(this)
        }
    }

    override fun onDestroy() {
        super.onDestroy()
        if (::mPresenter.isInitialized) {
            try {
                mPresenter.detach()
            } catch (e: UninitializedPropertyAccessException) {
                e.printStackTrace()
            }
        }

    }

    companion object {

        /**
         * 实例presenter
         * @param o
         * @param i
         * @param <T>
         * @return
        </T> */
        fun <T> getT(o: Any, i: Int): T? {
            try {
                return ((o.javaClass
                    .genericSuperclass as ParameterizedType).actualTypeArguments[i] as Class<T>)
                    .newInstance()
            } catch (e: java.lang.InstantiationException) {
                e.printStackTrace()
            } catch (e: IllegalAccessException) {
                e.printStackTrace()
            } catch (e: ClassCastException) {
                e.printStackTrace()
            }
            throw RuntimeException("can not create presenter!")
        }
    }
}