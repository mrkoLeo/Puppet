interface BaseView {
    fun showProgress(
        message: CharSequence = "请稍等",
        title: CharSequence = "",
        cancelable: Boolean = true
    )

    fun hideProgress()

    fun showMessage(msg: CharSequence)
}

interface BasePresent {
    fun attach(view: BaseView)

    fun detach()
}

class MvpBasePresent<V : BaseView> : BasePresent {
    private lateinit var view: V
    private var mView: WeakReference<V>? = null

    override fun attach(view: BaseView) {
        mView = WeakReference(view as V)
    }

    override fun detach() {
        mView = null
    }

    fun getView(): V {
        if (mView != null && mView!!.get() != null) {
            return mView!!.get()!!
        } else {
            throw IllegalStateException("Mvp View is null")
        }
    }
}

